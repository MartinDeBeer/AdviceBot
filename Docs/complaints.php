    <h3 id="procedure">OUR COMPLAINTS PROCEDURE</h3>
    <b>Who may lodge the complaint?</b>
    <ul>
        <li>The complaint may be lodged by you as a client, a nominated beneficiary or a lawful successor.</li>
</ul>   
    <b>What constitutes a complaint?</b>
    <ul>
        <li>
        A complaint relating to a financial service rendered by the FSP or Representative which may be lodged by you against AdviceBot or any of its representatives if you have:
        <ul>
            <li>been treated unfairly</li>
            <li>suffered prejudice or damage as a result of AdviceBot or its representative failing to comply with the provisions of the FAIS act.</li>
            <li>suffered prejudice or damages as a result of the wilful or negligent financial service rendered by the FSP or its representative.</li>
        </ul>
        </li>
    </ul>
	<b>How to complain:</b>
    <ol>
        <li>
        It is advisable to complain to the FSP or its representative first.
        <ul>
            <li>Phone our client relations team on +27 (051)446 0033</li>
            <li>Email: admin@advicebot.co.za</li>
        </ul>
        </li>
        <li>
        Ask us to look at the complaint. You will need to fill in our complaint form with the details.
        Our client relations team can help you fill in the form. Alternatively, our client relations team can send the form to you
        </li>
        <li>
        Our complaint form:
        <ul>
            <li>Complainant needs to sign the complaint form and return it to us</li>
        </ul>
        </li>
        <li>Complaint forms will be directed to the relevant departmental manager</li>
        <li>Complaints regarding unusual or suspicious transactions and fraud will be forwarded to the AdviceBot Compliance Officer</li>
    </ol>	
    <h4>RECEIPT OF THE COMPLAINT</h4>
    <b>Once you have lodged your complaint we will:</b>
    <ul>
        <li>Acknowledge receipt of the complaint in writing as soon as reasonably possible</li>
        <li>Investigate the complaint in a timely and fair manner</li>
        <li>
        Respond in writing to you with the outcome of our investigation
        </li>
        <li>If we are unable to address the complaint within 3 (three) weeks of receipt, we will provide you with an update</li>
        <li>
        Should we fail to resolve the complaint to your satisfaction within 6 (six) weeks, we will inform you of the following:
        <ul>
            <li>That you may refer your complaint to the FAIS Ombudsman if you still wish to pursue the matter</li>
            <li>that you must lodge your complaint with the FAIS Ombudsman within 6 (six) months of receipt of such notification</li>
        </ul>
        </li>
    </ul>	

