    <p>To setup a valid Will is one of the most important financial decisions you have to make.</p>
    <h3>If you die without a will:</h3>
    <ul>
        <li>Your estate will devolve according to the Intestate Succession Act</li>
        <li>Your estate will be divided amongst your surviving spouse, children, parents or siblings according to a set formula.</li>
        <li>Your estate can pay unnecessary taxes and costs.</li>
        <li>Inheritance of minors are paid to the State’s Guardian fund.</li>
        <li>Your loved ones can be left without proper care.</li>
        <li>You don’t have control over the appointment of an executor. (The person distributing your assets)</li>
    </ul>

    <h3><u>IFAA TIP:</u></h3>
    <p>When you pass away, your estate does not pay estate duty on assets that your spouse will inherit. (Section 4Q deduction) Any benefit from a life insurance policy that pays directly to a beneficiary will not incur any executor fees. It is therefore important to do proper planning when you draw up your Will. </p>
  
